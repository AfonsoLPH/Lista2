public class ExemploVar {
    public static void main(String[] args) {
        System.out.println("Isto funciona!");
//  declarando uma variavel
//  TIPO NOME;
//  TIPO=> int, float, double, String, char, boolean, short, byte, long...
//  NOME=> O/A DONO DO COD QUE ESCOLHE
//  para declarar uma variavel
        int numero;
//atribuir valor no espeaço de memória, o nome da variavél é o mesmo o conteúdo que muda.      
        numero = 2101;
 //    A letra "+" fora das aspas junta o texto.   
        System.out.println("O valor da variavel numero é " + numero);
    
    }
}